								d="M191.394 49.141c-.623 0-.822-.404-.442-.899l17.15-22.317c.38-.495.38-1.304 0-1.799L190.952 1.81c-.38-.495-.181-.899.442-.899h3.114c.624 0 1.442.407 1.817.905l16.798 22.304c.375.5.375 1.313 0 1.811l-16.798 22.305c-.375.498-1.193.905-1.817.905z"
							/>
						</g>
						<g>
							<path
								:fill="logoColor"
								d="M193.195 49.14h26.01c.623 0 1.133-.508 1.133-1.132v-1.87c0-.623-.51-1.133-1.134-1.133h-21.67zm4.34-44.092h21.67c.623 0 1.133-.51 1.133-1.134v-1.87c0-.622-.51-1.133-1.134-1.133h-26.009z"
							/>
						</g>
					</g>
				</svg>
			</span>
		</div>
		<div class="user-area flex" >
			<template v-if="isGuest">
				<router-link to="/login">Login</router-link>
				<router-link to="/signup">Signup</router-link>
			</template>
			<template v-else>
				<span class="logout" @click="emitLogout">Logout</span>
				<custom-avatar
					:size="40"
					:username="loggedinUser.username"
					:src="loggedinUser.imgUrl"
				/>
			</template>
		</div>
		<template v-if="!isHome">
			<i
				class="fas fa-bars sm-header-btn"
				@click="toggleNav"
				ref="nav-btn"
			></i>
			<div class="sm-header flex f-col" v-if="isShowNav">
				<div @click="goTo('/')" class="nav-link">Home Page</div>

				<div @click="goTo('board')" class="nav-link">Boards</div>

				<div @click="goTo('/login')" class="nav-link">Login</div>

				<div @click="goTo('/signup')" class="nav-link">Signup</div>
			</div>
		</template>
	</section>
</template>

<script>
import customAvatar from '@/cmps/custom-elements/custom-avatar.cmp'
export default {
	props: {
		loggedinUser: Object,
	},
	data() {
		return {
			isHome: false,
			isAtTop: true,
			isShowNav: false,
			isLoading: false
		}
	},
	computed: {
		isGuest() {
			return this.loggedinUser._id === 'guest' ? true : false;
		},
		headerClass() {
			return { home: this.isHome, scrolled: (this.isHome && !this.isAtTop), app: !this.isHome };
		},
		logoColor() {
			return (this.isHome) ? '#362C2C' : '#fefefe'
		}
	},
	methods: {
		emitLogout() {
			this.$emit('logout');
		},
		goToHome() {
			if (this.$route.name === 'home') return;
			this.$router.push('/');
		},
		checkPosition() {
			this.isAtTop = (window.scrollY === 0);
		},
		toggleNav() {
			this.isShowNav = !this.isShowNav;
			this.$refs['nav-btn'].classList.toggle('open')
		},
		goTo(dest) {
			this.isShowNav = false;
			this.$refs['nav-btn'].classList.remove('open')
			this.$router.push(`${dest}`)
		}
	},
	watch: {
		'$route.path'() {
			const page = this.$route.name;
			this.isHome = (page === 'home' || page === 'login' || page === 'signup');
			if(page !== 'home'){
				this.isLoading = true;
				setTimeout(()=>this.isLoading = false, 1700)
			}
		}
	},
	created() {
		const page = this.$route.name;
		this.isHome = (page === 'home' || page === 'login' || page === 'signup');
		window.addEventListener('scroll', this.checkPosition);
	},
	destroyed() {
		window.removeEventListener('scroll', this.checkPosition);
	},
	components: {
		customAvatar
	}
};
</script>